package day08_IfStatements;

public class IfStatement_Example {
    public static void main(String[] args) {

        int n = 25;

        if(n > 0) {
            System.out.println(n + " is positive");
        }

        if(n < 0) {
            System.out.println(n + " is negative");
        }

        if(n == 0 ) {
            System.out.println(n + " is zero");
        }
    }
}
