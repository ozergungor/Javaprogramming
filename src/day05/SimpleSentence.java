package day05;

public class SimpleSentence {
    public static void main(String[] args) {
        String hello = "Hi, my name is";
        String name = "Güngör";
        String andIam = "and I am";
        int age = 50;
        String yearsOld = "years old";

        System.out.print("hello = " + hello);
        System.out.println("name = " + name);
        System.out.println("andIam = " + andIam);
        System.out.println("age = " + age);
        System.out.println("yearsOld = " + yearsOld);
    }
}
