package day09_IfElseAndMultiBranchStatements;

public class MaxNumber {
    public static void main(String[] args) {

        int a = 55,
                b = 40;

        if (a > b) {
            System.out.println(a + " is the maximum number");
        } else {
            System.out.println(b + " is the maximum number");


        }
    }
}
