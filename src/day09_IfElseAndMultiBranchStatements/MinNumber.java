package day09_IfElseAndMultiBranchStatements;

public class MinNumber {
    public static void main(String[] args) {

        int a = -125,
                b = 46;

        if(a < b){
            System.out.println(a + " is the minimum number");
        }else {
            System.out.println(b + " is the minimum number");
        }
    }
}
