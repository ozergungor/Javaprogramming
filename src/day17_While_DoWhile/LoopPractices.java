package day17_While_DoWhile;

public class LoopPractices {

    public static void main(String[] args) {

        for (int i = 0; i < 10 ; i++) {

            System.out.println(i);

        }        int j =1;
        while(j<10){

            j++;
            System.out.println(j);

        }

        int k = 1;
        do{
            System.out.println(k);
            k++;
        }while(k < 10);






    }
}
